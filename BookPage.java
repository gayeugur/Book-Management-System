    private void AddBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBookButtonActionPerformed

        AddBookButton.setBackground(Color.GRAY);
        boolean sec;
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/db", "gaye", "gaye");
            ps = con.prepareStatement("INSERT INTO BOOKINFORMATION (BOOKID,NAME,AUTHOR,STOCKSTATUS,BOOKTYPE) VALUES (?,?,?,?,?)");
            String secilen = (String) ComboType.getSelectedItem();
            ps.setInt(1, Integer.parseInt(idTextField.getText()));
            ps.setString(2, NameTextField.getText());
            ps.setString(3, AuthorTextField.getText());
            if (AvailableButton.isSelected()) {
                sec = true;
            } else {
                sec = false;
            }
            ps.setBoolean(4, sec);
            ps.setString(5, secilen);
            ps.executeUpdate();
            if (secilen.equals("Educational Book")) {
                EducationalBook.Educationalbooks.add(NameTextField.getText());
                EducationalBook.topics.add(jTextField1.getText());
            } else if (secilen.equals("Scientific Book")) {
                ScientificBooks.ScientificBooksName.add(NameTextField.getText());
            }
            Book.booksName.add(NameTextField.getText());
            JOptionPane.showMessageDialog(rootPane, "Book insertion is successful");
        } catch (SQLIntegrityConstraintViolationException sqle) {
            JOptionPane.showMessageDialog(null, "Book id must be different");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "There must be at least 1 stock for book additions");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Database is not connection");
        }
    }//GEN-LAST:event_AddBookButtonActionPerformed

