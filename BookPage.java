    private void txtFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtFileActionPerformed

        boolean kon = false;
        BufferedWriter bufferedWriter = null;

        File file = new File("C:/Users/gayeu/Desktop/book.txt");
        FileWriter fileWriter;

        try {
            fileWriter = new FileWriter(file, true);
            bufferedWriter = new BufferedWriter(fileWriter);
            if (AvailableButton.isSelected()) {
                kon = true;
            }
            bufferedWriter.write("Book Ä°d: " + idTextField.getText() + "  ");
            bufferedWriter.write("Book Name: " + NameTextField.getText() + "  ");
            bufferedWriter.write("Book Writer:" + AuthorTextField.getText() + "  ");
            bufferedWriter.write("Book Available:" + kon + " ");
            bufferedWriter.write("Book Type :" + ComboType.getSelectedItem() + "  ");
            bufferedWriter.newLine();
            bufferedWriter.flush();
            bufferedWriter.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Wrong ");
        }
        int option = JOptionPane.showConfirmDialog(null, "Save successfully,Do you want to read?", "Choose", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            try {

                BufferedReader reader = null;
                reader = new BufferedReader(new FileReader(file));
                String satir = reader.readLine();

                while (satir != null) {
                    JOptionPane.showMessageDialog(rootPane, satir);
                    satir = reader.readLine();
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Something wrong");
            }
        }

    }//GEN-LAST:event_txtFileActionPerformed

