    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed

        dtm.getDataVector().removeAllElements();
        String requested = RequestedText.getText();
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/db", "gaye", "gaye");
            statement = con.createStatement();
            result = statement.executeQuery("SELECT * FROM BOOKINFORMATION  WHERE NAME='" + requested + "'");
            while (result.next()) {
                int id = result.getInt("BOOKID");
                String name = result.getString("NAME");
                String writer = result.getString("AUTHOR");
                boolean stock = result.getBoolean("STOCKSTATUS");
                String type = result.getString("BOOKTYPE");
                dtm.addRow(new Object[]{id, name, writer, stock, type});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something is wrong");
        }

    }//GEN-LAST:event_SearchButtonActionPerformed

