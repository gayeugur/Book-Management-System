    private void Search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_ButtonActionPerformed
        if (jRadioButton1.isSelected() == false && jRadioButton2.isSelected() == false) {
            new EducationalBook(null, null, 0, 0, true, null).BookSearch(jTextField1.getText());
        } else if (jRadioButton1.isSelected() == true || jRadioButton2.isSelected() == true) {
            if (jRadioButton1.isSelected() == true) {
                new EducationalBook(null, null, 0, 0, true, null).BookSearch(jTextField1.getText(), EducationalBook.Educationalbooks);
            } else {
                new EducationalBook(null, null, 0, 0, true, null).BookSearch(jTextField1.getText(), ScientificBooks.ScientificBooksName);
            }
        }
    }//GEN-LAST:event_Search_ButtonActionPerformed
