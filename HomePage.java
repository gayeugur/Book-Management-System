    private void Login_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login_ButtonActionPerformed
        if (check_admin.isSelected()==true) {
            admin_check = true;
            addpass_button.setVisible(true);
            jLabel4.setVisible(true);
            try {
                con = DriverManager.getConnection("jdbc:derby://localhost:1527/db", "gaye", "gaye");
                String userName = usernametext.getText();
                String password = PasswordText.getText();
                String admin_pas = addpass_button.getText();
                statement = con.createStatement();
                result = statement.executeQuery("SELECT USERNAME,PASSWORD,ADMINPASS FROM USERS");
                while (result.next()) {
                    if (result.getString("USERNAME").equals(userName) && result.getString("PASSWORD").equals(password) && result.getString("ADMINPASS").equals(admin_pas) && admin_pas != null && userName != null && password != null) {
                        JOptionPane.showMessageDialog(rootPane, "Log in succesfully,welcome to application");
                        new BookPage().setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Wrong user name or password");
                    }
                    break;
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Database is not connection");
            }
        } else {
            try {
                con = DriverManager.getConnection("jdbc:derby://localhost:1527/db", "gaye", "gaye");
                String userName = usernametext.getText();
                String password = PasswordText.getText();
                statement = con.createStatement();
                result = statement.executeQuery("SELECT USERNAME,PASSWORD FROM USERS");
                while (result.next()) {
                    if (result.getString("USERNAME").equals(userName) && result.getString("PASSWORD").equals(password) && userName != null && password != null) {
                        JOptionPane.showMessageDialog(rootPane, "Log in succesfully,welcome to application");
                        new BookPage().setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Wrong user name or password");
                    }
                    break;
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Database is not connection");
            }
        }
    }//GEN-LAST:event_Login_ButtonActionPerformed

    private void CompleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CompleteButtonActionPerformed
        boolean admin = false;

        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/db", "gaye", "gaye");
            ps = con.prepareStatement("INSERT INTO USERS (NAME,USERNAME,PASSWORD,BOOKNAME,ADMIN,ADMINPASS) VALUES (?,?,?,?,?,?)");
            if (AdminButton.isSelected()) {
                RandomPassword();
                admin = true;
                admin_pass_label.setVisible(true);
                jLabel3.setVisible(true);
                admin_pass_label.setText(admin_pass);
            } else if (UserButton.isSelected()) {
                admin = false;
                admin_pass = null;
            }
            ps.setString(1, nametext.getText());
            ps.setString(2, usernametext.getText());
            ps.setString(3, PasswordText.getText());
            ps.setString(4, (String) SelectedBook.getSelectedItem());
            ps.setBoolean(5, admin);
            ps.setString(6, admin_pass);
            if (usernametext.getText() == null || PasswordText.getText() == null) {
                JOptionPane.showMessageDialog(rootPane, "Please write all the information");
            }
            ps.executeUpdate();

            JOptionPane.showMessageDialog(rootPane, "Record created is successful");
            nametext.setVisible(false);
            NameLabel.setVisible(false);
            Label.setVisible(false);
            CompleteButton.setVisible(false);
            Login_Button.setVisible(true);
            AdminButton.setVisible(false);
            UserButton.setVisible(false);
            SelectedBook.setVisible(false);
            jLabel3.setVisible(false);
            admin_pass_label.setVisible(false);
            ///guncelleme
            try {
                boolean kont = false;
                con = DriverManager.getConnection("jdbc:derby://localhost:1527/db", "gaye", "gaye");
                ps = con.prepareStatement("UPDATE BOOKINFORMATION SET STOCKSTATUS=? WHERE NAME=?");
                ps.setBoolean(1, kont);
                ps.setString(2, (String) SelectedBook.getSelectedItem());

                ps.executeUpdate();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Bir hata oluştu");

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Database is not connection");
        }
    }//GEN-LAST:event_CompleteButtonActionPerformed

